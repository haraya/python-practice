"""
Define three variables: principal, rate, and time, representing the principal amount, interest rate, and time in years.
Calculate and print the simple interest using the formula
interest = (principal×rate×time)/100.
"""

#Variables
principal_amount = 50
rate_amount = 100
time_years = 5

#Formula
interes = (principal_amount * rate_amount * time_years) / 100

#Printing
print(f"The interest is {interes}")